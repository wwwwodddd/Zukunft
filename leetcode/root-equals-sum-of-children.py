# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class Solution:
    def checkTree(self, x: Optional[TreeNode]) -> bool:
        return x.left.val + x.right.val == x.val