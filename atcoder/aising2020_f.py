def G(n):
	a = [[0, 0, 0, 11, 297, 3003, 16445, 56485, 130923, 212681, 246047, 202380, 115920, 44032, 9984, 1024],
		[0, 0, 1, 68, 1068, 8008, 35750, 105248, 215644, 316184, 335137, 255420, 136752, 48896, 10496, 1024]][n & 1]
	n //= 2
	z = 0
	c = 1
	for i in range(16):
		z = (z + c * a[i]) % 1000000007
		c = c * (n - i) // (i + 1)
	return z

for i in range(int(input())):
	print(G(int(input())))